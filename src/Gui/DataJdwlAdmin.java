    private void UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbahActionPerformed
        // TODO add your handling code here:
        JadwalModel mhs = new JadwalModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Jadwal"));
        
        String test = JOptionPane.showInputDialog("Masukkan Hari yang Baru");
        if(test.length()>0){
            mhs.updateHari(test,id);
            DataJdwlAdmin dtmhs = new DataJdwlAdmin();
            dispose();
            dtmhs.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_UbahActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        JadwalModel mhs = new JadwalModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Jadwal"));
        
        if(id>-1){
            mhs.deleteJadwal(id);
            DataJdwlAdmin dtmhs = new DataJdwlAdmin();
            dispose();
            dtmhs.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_HapusActionPerformed

    private void TambahJwlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahJwlActionPerformed
        Jadwal ent = new Jadwal();
        JadwalModel mdl = new JadwalModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Jadwal yang Baru"));
        String hari = JOptionPane.showInputDialog("Masukkan Hari yang Baru");
        String jam = JOptionPane.showInputDialog("Masukkan Jam yang Baru");
        if(hari.length()>0){
            mdl.insertData(new Jadwal(id,hari,jam));
            DataJdwlAdmin dtmhs = new DataJdwlAdmin();
            dispose();
            dtmhs.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_TambahJwlActionPerformed

