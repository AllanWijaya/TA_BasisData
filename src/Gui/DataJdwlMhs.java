    private void tampil(){
        try{
            KoneksiDb knksi = new KoneksiDb();
            Connection conn = knksi.getconection();
            stat=conn.createStatement();
            rs=stat.executeQuery("select nama_mhs,nama_matkul,nama_dosen,hari,jam,nama_ruang from mahasiswa "
                    + "join detail_krs on mahasiswa.id_mhs=detail_krs.id_mhs "
                    + "join kartu_studi on kartu_studi.id_ks=detail_krs.id_ks "
                    + "join matkul on matkul.id_matkul=kartu_studi.id_matkul "
                    + "join dosen on dosen.id_dosen=kartu_studi.id_dosen "
                    + "join ruang on ruang.id_ruang=kartu_studi.id_ruang "
                    + "join jadwal on jadwal.id_jadwal=kartu_studi.id_jadwal "
                    + "where npm='"+JOptionPane.showInputDialog("Masukkan NPM untuk Konfirmasi")+"'");
            while(rs.next()){
                String[] row ={rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                model.addRow(row);                
            }  
        }catch(Exception e){
            System.out.println(e);
        }
    }
    

