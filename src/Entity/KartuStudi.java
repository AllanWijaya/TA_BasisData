package Entity;

public class KartuStudi {
    private int id_ks,id_dsn,id_mk,id_jadwal,id_ruang;
    private float ipk;

    public KartuStudi(){}

    public int getId_ks() {
        return id_ks;
    }

    public void setId_ks(int id_ks) {
        this.id_ks = id_ks;
    }

    public int getId_dsn() {
        return id_dsn;
    }

    public void setId_dsn(int id_dsn) {
        this.id_dsn = id_dsn;
    }

    public int getId_mk() {
        return id_mk;
    }

    public void setId_mk(int id_mk) {
        this.id_mk = id_mk;
    }

    public int getId_jadwal() {
        return id_jadwal;
    }

    public void setId_jadwal(int id_jadwal) {
        this.id_jadwal = id_jadwal;
    }

    public int getId_ruang() {
        return id_ruang;
    }

    public void setId_ruang(int id_ruang) {
        this.id_ruang = id_ruang;
    }

    public float getIpk() {
        return ipk;
    }

    public void setIpk(float ipk) {
        this.ipk = ipk;
    }
    
}
