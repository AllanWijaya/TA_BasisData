    private void UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbahActionPerformed
        // TODO add your handling code here:
        MahasiswaModel mhs = new MahasiswaModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Mahasiswa"));
        
        String test = JOptionPane.showInputDialog("Masukkan Nama Mahasiswa yang Baru");
        if(test.length()>0){
            mhs.updateNama(test,id);
            DataMhsAdmin dtmhs = new DataMhsAdmin();
            dispose();
            dtmhs.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_UbahActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        MahasiswaModel mhs = new MahasiswaModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Mahasiswa"));
        
        if(id>-1){
            mhs.deleteMahasiswa(id);
            DataMhsAdmin dtmhs = new DataMhsAdmin();
            dispose();
            dtmhs.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_HapusActionPerformed

    private void TambahMhsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahMhsActionPerformed
        MahasiswaModel mdl = new MahasiswaModel();
        int id = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Mahasiswa yang Baru"));
        String nama = JOptionPane.showInputDialog("Masukkan Nama Mahasiswa yang Baru");
        String nip = JOptionPane.showInputDialog("Masukkan NPM yang Baru");
        String alamat = JOptionPane.showInputDialog("Masukkan Alamat yang Baru");
        String notelp = JOptionPane.showInputDialog("Masukkan No Telp yang Baru");
        String pw = JOptionPane.showInputDialog("Masukkan Password yang Baru");
        if(nama.length()>0){
            mdl.insertData(new Mahasiswa(id,nama,nip,alamat,notelp,pw));
            DataMhsAdmin dtmhs = new DataMhsAdmin();
            dispose();
            dtmhs.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Data Kosong");
        }
    }//GEN-LAST:event_TambahMhsActionPerformed

